public class ChunkProviderParameter
{
	public int id;

	public string name;

	public float val;

	public float minVal;

	public float maxVal;

	public ChunkProviderParameter(int _id, string _name, float _val, float _minVal, float _maxVal)
	{
		id = _id;
		name = _name;
		val = _val;
		minVal = _minVal;
		maxVal = _maxVal;
	}
}
